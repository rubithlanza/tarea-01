/*9. Celsius a Fahrenheit:
Pida una temperatura en grados Celsius y convierta a grados Fahrenheit. */

const celsius = parseInt(prompt('Ingresa la cantidad de gados celsius:'));

const fahrenheit = ((celsius * 1.8) + 32);

alert(`La cantidad de grados celsuis a fahrenheit es de: ${fahrenheit}`)
console.log('Los grados celsuis', celsius, 'a grados fahrenheit es de:', fahrenheit);
